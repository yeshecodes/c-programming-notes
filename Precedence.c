#include <stdio.h>

int main(){
    /*
    
    () - Parentheses
    *, /, % - Multiplication, Division, Modulus
    +, - - Addition, Subtraction
    >, <, >=, <= - Comparison
    ==, != - Equality
    && - Logical AND
    || - Logical OR
    = - Assignment
    */

    int result1 = 10 - 3 + 7; //14
    int result2 = (2 + 3) * 4; // 20
    int result3 = 2 + 3 * 4; // 14
    printf("%d\n",result1);
    printf("%d\n", result2);
    printf("%d\n", result3);
}